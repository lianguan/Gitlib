namespace Gitlib\Model;

class Tag extends Object
{
    protected $name;

    public function getName()
    {
        return $this->name;
    }

    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    public function isTag()
    {
        return true;
    }
}
